const exampleCard = document.getElementById("examplesCard");

let exampleRender = true;

function ExampleShow() {
  if (exampleRender) {
    Examples.forEach((example) => {
      exampleCard.innerHTML += ` <div class="col-lg-3 col-md-4 col-sm-6 mb-4">
            <div class="card">
              <video class="card-img-top" controls>
                <source src="${example.videoUrl}" type="video/mp4">
              </video>
              <div class="card-body">
                <h5 class="card-title">${example.title}</h5>
                <p class="card-text">${example.description}</p>
                <p class="card-text mt-2">${example.details}</p>
              </div>
            </div>
          </div>`;
      exampleRender.true;
      document.getElementById("examplesCard").style.display = "flex";
    });
  } else {
    exampleCard.innerHTML = "";
    exampleRender = false;
  }
}

// Sayfa Yüklendiginde fonksiyonu tetikle
document.addEventListener("DOMContentLoaded", (e) => {
  ExampleShow();
});
